    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/library_management","root","");
            
            String sql = "SELECT * FROM librarian WHERE username = ? && password = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1,enteruser.getText());
            pst.setString(2,enterpassword.getText());
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
               lib_id.setText(rs.getString("Librarian_id"));
               username.setText(rs.getString("username"));
               password.setText(rs.getString("password"));
               name.setText(rs.getString("name"));
               nic.setText(rs.getString("nic"));
               contactno.setText(rs.getString("contact_number"));
               address.setText(rs.getString("address"));
               email.setText(rs.getString("email"));
            }
            else{
                JOptionPane.showMessageDialog(null,"Username or Password is incorrect!");
            }
        }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

