    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
           Class.forName("com.mysql.jdbc.Driver");
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/library_management","root","");
           
           String sql = "UPDATE librarian SET username =?,password =?,name = ?,nic =?,contact_number =?,address =?,email =? WHERE librarian_id = ?";
           PreparedStatement pstmt = con.prepareStatement(sql);
           
           pstmt.setString(1,username.getText());
           pstmt.setString(2,password.getText());
           pstmt.setString(3,name.getText());
           pstmt.setString(4,nic.getText());
           pstmt.setInt(5,Integer.parseInt(contactno.getText()));
           pstmt.setString(6,address.getText());
           pstmt.setString(7,email.getText());
           pstmt.setInt(8,Integer.parseInt(lib_id.getText()));
           
           pstmt.executeUpdate();
           JOptionPane.showMessageDialog(null,"Successfully Updated");
           con.close();
       
       }catch(Exception e)
       {
           JOptionPane.showMessageDialog(null,e);
       }
    }//GEN-LAST:event_jButton2ActionPerformed

