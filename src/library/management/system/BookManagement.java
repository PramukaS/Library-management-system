    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
           Class.forName("com.mysql.jdbc.Driver");
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/library_management","root","");
           
           String sql = "INSERT INTO book (id,isbm_number,title,author,publisher,price)" + "values(?,?,?,?,?,?)";
           
           PreparedStatement pstmt = con.prepareStatement(sql);
           pstmt.setString(1,null);
           pstmt.setInt(2,Integer.parseInt(isbm.getText()));
           pstmt.setString(3,title.getText());
           pstmt.setString(4,author.getText());
           pstmt.setString(5,publisher.getText());
           pstmt.setDouble(6,Double.parseDouble(price.getText()));
           
           pstmt.executeUpdate();
           JOptionPane.showMessageDialog(null,"Successfully Inserted!");
           con.close();
       
       }catch(Exception e)
       {
           JOptionPane.showMessageDialog(null,e);
       }
       showTableData();
    }//GEN-LAST:event_jButton1ActionPerformed

