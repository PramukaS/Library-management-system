    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        try{
           Class.forName("com.mysql.jdbc.Driver");
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/library_management","root","");
        
           String sql = "SELECT * FROM librarian WHERE librarian_id = ?";
           
           PreparedStatement pstmt = con.prepareStatement(sql);
           pstmt.setString(1,(lib_id.getText()));
           
           ResultSet rs = pstmt.executeQuery();
           
           if(rs.next())
           {
               username.setText(rs.getString("username"));
               password.setText(rs.getString("password"));
               name.setText(rs.getString("name"));
               nic.setText(rs.getString("nic"));
               contactno.setText(rs.getString("contact_number"));
               address.setText(rs.getString("address"));
               email.setText(rs.getString("email"));
           }
           else{
               JOptionPane.showMessageDialog(null,"No Librarian Found!");
               con.close();
           }
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
        //showTableData();
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
           Class.forName("com.mysql.jdbc.Driver");
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/library_management","root","");
           
           String sql = "INSERT INTO librarian (librarian_id,username,password,name,nic,contact_number,address,email)" + "values(?,?,?,?,?,?,?,?)";
           
           PreparedStatement pstmt = con.prepareStatement(sql);
           pstmt.setString(1,null);
           pstmt.setString(2,username.getText());
           pstmt.setString(3,password.getText());
           pstmt.setString(4,name.getText());
           pstmt.setString(5,nic.getText());
           pstmt.setInt(6,Integer.parseInt(contactno.getText()));
           pstmt.setString(7,address.getText());
           pstmt.setString(8,email.getText());

           
           pstmt.executeUpdate();
           JOptionPane.showMessageDialog(null,"Librarian Added Successfully!");
           con.close();
       
       }catch(Exception e)
       {
           JOptionPane.showMessageDialog(null,e);
       }
       showTableData();
    }//GEN-LAST:event_jButton1ActionPerformed

